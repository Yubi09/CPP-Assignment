#include<bits/stdc++.h>
using namespace std;

int main() {
  cout << "Two ways to output 'Physics' and 'Wallah' in different line in C++ are: " << endl;
  // Using endl
  cout << "Physics" << endl;
  cout << "Wallah" << endl;

  // Using '\n'
  cout << "Physics\n";
  cout << "Wallah\n";
  return 0;
}